//setting variables
let x = 10;
let y = 5;

//printing  to console
console.log(x + y);  //sum of x and y
console.log(x - y);  //subtraction of x and y
console.log(x * y);  //multiplication of x and y
console.log(x / y);  //x divided by y
console.log(x % y);  //remainder of x and y
console.log(x == y); //does x = y?

